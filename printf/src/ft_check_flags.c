#include "libftprintf.h"

void	ft_check_flags(t_print *vars, char **f)
{
	if (*(*f) == '-' || *(*f) == '+' || *(*f) == '#' || *(*f) == '0' ||
			*(*f) == ' ')
	{
		(*vars).lttr = *(*f);
		(*f)++;
	}
}
