#include "libftprintf.h"

void	ft_check_prec(t_print *vars, char **f)
{
	if (*(*f) == '.')
	{
		(*f)++;
		while (*(*f) >= '0' && *(*f) <= '9')
		{
			(*vars).prec *= 10;
			(*vars).prec += *(*f) - '0';
			(*f)++;
		}
	}
}
