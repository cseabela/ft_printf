#include "libftprintf.h"

void	ft_check_width(t_print *vars, char **f)
{
	while (*(*f) >= '0' && *(*f) <= '9')
	{
		(*vars).width *= 10;
		(*vars).width += *(*f) - '0';
		(*f)++;
	}
}
